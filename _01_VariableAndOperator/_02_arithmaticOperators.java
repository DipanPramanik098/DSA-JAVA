
public class _02_arithmaticOperators {
   public static void main(String[] args) {
    int a = 7;
    int b = 9;

    int c = a + b;
    System.out.println(c);

    c= a-b;
    System.out.println(c);

    int d = a*b;
    System.out.println(d);

    d=a/b;
    System.out.println(d);

    d = a%b;
    System.out.println(d);
    
   } 
}
