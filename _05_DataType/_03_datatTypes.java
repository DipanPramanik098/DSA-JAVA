

public class _03_datatTypes {
    public static void main(String[] args) {
        byte a = 10; // 1 byte
        short b = 10; // 2 bytes
        int c = 10; // 4 bytes
        long d = 10; // 8 bytes

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }    
}
