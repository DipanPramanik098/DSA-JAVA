
public class _06_ODDeven {
    public static void main(String[] args) {
        int n = 761;
        if (n > 100) {
            System.out.println("yes");
        }
        if (n % 2 == 0) {
            System.out.println("Even");
        } else {
            System.out.println("Odd");
        }
    }
}
