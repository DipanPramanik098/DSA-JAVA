import java.util.Scanner;

public class _11_whileLoop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        int i=1;
        while( i<=n ){
            System.out.println("Dipan Pramanik");
            // i=i+1
            i++;
        }
    }    
}
